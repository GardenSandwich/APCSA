package Unit_1._1_4.personalizedtshirts;

public class Tshirt {
    public static void main(String[] args) {
        int cost = 22;
        System.out.println("The cost of the t-shirt is $" + cost + ".");

        cost++;

        System.out.println("A personalized t-shirt is $" + cost + ".");

        cost--;

        System.out.println("Without personalization, the t-shirt costs " + cost + ".");

    }
}